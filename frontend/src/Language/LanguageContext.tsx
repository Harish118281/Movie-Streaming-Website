const translateCount = (text: string, t: (key: string) => string) => {
  const match = text.match(/^(\d+)\s+(Language|Languages)$/);

  if (!match) {
    return "";
  }

  return `${match[1]} ${t(match[2])}`;
};

const translateText = (text: string, dictionary: TranslationMap) => {
  if (dictionary[text]) {
    return dictionary[text];
  }

  const simpleTranslate = (key: string) => dictionary[key] || key;
  const countedText = translateCount(text, simpleTranslate);

  if (countedText) {
    return countedText;
  }

  return text;
};

const LanguageContext = createContext<LanguageContextValue | null>(null);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>(getInitialLanguage);

  const setLanguage = (nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage);
    window.localStorage.setItem(STORAGE_KEY, nextLanguage);
  };

  const value = useMemo<LanguageContextValue>(() => {
    const selectedOption =
      LANGUAGE_OPTIONS.find((option) => option.code === language) || LANGUAGE_OPTIONS[0];
    const dictionary = translations[language];

    return {
      language,
      languageLabel: selectedOption.nativeLabel,
      tmdbLanguage: selectedOption.tmdbLanguage,
      options: LANGUAGE_OPTIONS,
      setLanguage,
      t: (text: string) => translateText(text, dictionary),
    };
  }, [language]);

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => {
  const context = useContext(LanguageContext);

  if (!context) {
    throw new Error("useLanguage must be used inside LanguageProvider");
  }

  return context;
};
